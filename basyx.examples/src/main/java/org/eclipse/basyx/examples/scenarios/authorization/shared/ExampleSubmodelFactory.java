package org.eclipse.basyx.examples.scenarios.authorization.shared;

import org.eclipse.basyx.aas.metamodel.map.descriptor.CustomId;
import org.eclipse.basyx.submodel.metamodel.map.Submodel;
import org.eclipse.basyx.submodel.metamodel.map.submodelelement.dataelement.property.Property;
import org.eclipse.basyx.submodel.metamodel.map.submodelelement.dataelement.property.valuetype.ValueType;

/**
 * Factory to create an example {@link Submodel}.
 *
 * @author wege
 */
public class ExampleSubmodelFactory {
	public Submodel create() {
		final Submodel submodel = new Submodel("exampleSubmodel", new CustomId("submodel.example"));

		final Property consumptionProperty = new Property();
		consumptionProperty.setIdShort("consumption");
		consumptionProperty.set(400.123, ValueType.Float);
		submodel.addSubmodelElement(consumptionProperty);

		return submodel;
	}
}
